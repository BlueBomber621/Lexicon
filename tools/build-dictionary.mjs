// build-dictionary.mjs — packs the ENABLE1 word list into js/data/lexicon.data.js
//
// Usage: node tools/build-dictionary.mjs <path-to-enable1.txt>
//
// Filters to words of 1–11 letters (the composing stick's capacity),
// gzips the newline-joined list, and emits it as a base64 string constant.
// The game decompresses it at load via the browser's DecompressionStream API.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { gzipSync } from 'node:zlib';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const MAX_LEN = 11;

const src = process.argv[2];
if (!src) {
  console.error('Usage: node tools/build-dictionary.mjs <path-to-enable1.txt>');
  process.exit(1);
}

const words = readFileSync(src, 'utf8')
  .split(/\r?\n/)
  .map((w) => w.trim().toUpperCase())
  .filter((w) => w.length >= 1 && w.length <= MAX_LEN && /^[A-Z]+$/.test(w));

const payload = gzipSync(words.join('\n'), { level: 9 });
const b64 = payload.toString('base64');

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'js', 'data');
mkdirSync(outDir, { recursive: true });

const out =
  `// Generated by tools/build-dictionary.mjs — do not edit by hand.\n` +
  `// ${words.length} words (1-${MAX_LEN} letters), gzip+base64, ` +
  `${(payload.length / 1024).toFixed(0)} KB compressed.\n` +
  `const LEXICON_GZ_B64 = '${b64}';\n`;

writeFileSync(join(outDir, 'lexicon.data.js'), out);
console.log(`Packed ${words.length} words -> js/data/lexicon.data.js (${(out.length / 1024).toFixed(0)} KB file)`);
