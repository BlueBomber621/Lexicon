// build-dictionary.mjs — packs an English word list into js/data/lexicon.data.js
//
// Usage: node tools/build-dictionary.mjs <path-to-wordlist.txt>
//
// Source is a newline-delimited word list — currently the dwyl english-words
// `words_alpha.txt`, which (unlike the old ENABLE1 Scrabble list it replaced)
// includes proper nouns like months and weekdays plus a lot of general
// vocabulary, so words like JULY, TUESDAY and INTERNET validate. We keep words
// of MIN_LEN..MAX_LEN letters, fold in a small hand-curated SUPPLEMENT of
// modern words the source still predates, gzip the newline-joined list, and
// emit it as a base64 string constant. The game decompresses it at load via
// the browser's DecompressionStream API (see js/dictionary.js).
//
// Length bounds: the composing stick has 11 physical slots (CFG.STICK_SLOTS),
// but multi-letter sorts (Œ→CE, &→AND) let a full stick spell more letters
// than slots used, so a resolved word can run longer — MAX_LEN 20 matches
// CFG.MAX_WORD_LEN. MIN_LEN is 2 so single letters never count as words
// (words_alpha lists all 26, and a 1-letter word makes every tile playable).

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { gzipSync } from 'node:zlib';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const MIN_LEN = 2;
const MAX_LEN = 20;

// Common modern words the source list predates. Kept here (not merged into the
// big list on disk) so it's obvious at a glance what we added by hand, and easy
// to extend later. Overlaps with the source are harmless — the merge dedups.
const SUPPLEMENT = [
  // web & social
  'WEBSITE', 'WEBSITES', 'WEBPAGE', 'WEBPAGES', 'WEBCAM', 'WEBCAMS',
  'HYPERLINK', 'HYPERLINKS', 'BLOG', 'BLOGS', 'BLOGGER', 'BLOGGERS',
  'BLOGGING', 'VLOG', 'VLOGS', 'VLOGGER', 'PODCAST', 'PODCASTS',
  'HASHTAG', 'HASHTAGS', 'USERNAME', 'USERNAMES', 'EMAILS', 'EMAILING',
  'RETWEET', 'RETWEETS', 'UNFOLLOW', 'UNFOLLOWED',
  // devices
  'SMARTPHONE', 'SMARTPHONES', 'SMARTWATCH', 'SMARTWATCHES', 'LAPTOPS',
  'DESKTOPS', 'TOUCHSCREEN', 'TOUCHSCREENS', 'TOUCHPAD', 'TOUCHPADS',
  'MOUSEPAD', 'MOUSEPADS',
  // media & content
  'SELFIE', 'SELFIES', 'EMOJI', 'EMOJIS', 'EMOTICON', 'EMOTICONS',
  'MEME', 'MEMES', 'GIFS', 'PLAYLIST', 'PLAYLISTS', 'TEXTED', 'TEXTING',
  'APPS', 'AVATARS',
  // security, data, infrastructure
  'MALWARE', 'SPYWARE', 'RANSOMWARE', 'FIREWALLS', 'CYBERSECURITY',
  'CHATBOT', 'CHATBOTS', 'METADATA', 'GEOLOCATION', 'AUTOCOMPLETE',
  'WORKFLOW', 'WORKFLOWS', 'MULTIPLAYER',
  // tech & finance
  'BITCOIN', 'BLOCKCHAIN', 'NANOTECH', 'WIFI',
];

const src = process.argv[2];
if (!src) {
  console.error('Usage: node tools/build-dictionary.mjs <path-to-wordlist.txt>');
  process.exit(1);
}

const clean = (w) => w.trim().toUpperCase();
const keep = (w) => w.length >= MIN_LEN && w.length <= MAX_LEN && /^[A-Z]+$/.test(w);

const fromFile = readFileSync(src, 'utf8').split(/\r?\n/).map(clean).filter(keep);
const supp = SUPPLEMENT.map(clean).filter(keep);

// Dedup + sort: a stable, alphabetical list keeps output deterministic and
// makes findReadingFromOptions resolve ambiguous readings (wildcards, sorts,
// substitution Books) to the alphabetically-first word — same as the old
// already-sorted ENABLE1 list did.
const words = [...new Set([...fromFile, ...supp])].sort();

const payload = gzipSync(words.join('\n'), { level: 9 });
const b64 = payload.toString('base64');

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'js', 'data');
mkdirSync(outDir, { recursive: true });

const out =
  `// Generated by tools/build-dictionary.mjs — do not edit by hand.\n` +
  `// ${words.length} words (${MIN_LEN}-${MAX_LEN} letters), gzip+base64, ` +
  `${(payload.length / 1024).toFixed(0)} KB compressed.\n` +
  `const LEXICON_GZ_B64 = '${b64}';\n`;

writeFileSync(join(outDir, 'lexicon.data.js'), out);
console.log(`Packed ${words.length} words -> js/data/lexicon.data.js (${(out.length / 1024).toFixed(0)} KB file)`);
console.log(`  from file: ${fromFile.length}, supplement: ${supp.length}, unique: ${words.length}`);
